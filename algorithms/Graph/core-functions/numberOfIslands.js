const numIslands = (grid) => {
	let len = grid.length
	let result = 0
	if (len === 0 || !grid[0]) return result
	let len1 = grid[0].length
	for (let i = 0; i < len; i++) {
		for (let j = 0; j < len1; j++) {
			if (grid[i][j] === '1') {
				result++ // encounter an island, count++
				dfs(grid, i, j, len, len1) // turn all its neighbors into 0s 
			}
		}
	}
	return result
}

const dfs = (grid, r, c) => {
	if (r >= grid.length || c >= grid[0].length || r < 0 || c < 0) return
	if (grid[r][c] === '1') {
		grid[r][c] = '0' // signal visited 
		dfs(grid, r - 1, c) // b: neighbor of vertice a, check all neighbors of b
		dfs(grid, r + 1, c)
		dfs(grid, r, c - 1)
		dfs(grid, r, c + 1)
	}
}

